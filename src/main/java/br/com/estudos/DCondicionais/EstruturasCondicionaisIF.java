package br.com.estudos.DCondicionais;

public class EstruturasCondicionaisIF {
	public static void main(String[] args) {
		
		// IF (SE)
		//Estrutura: if(condição booleana) {}
		if(true) {
			System.out.println("tegdgcgcd");
		}
		
		//saber que pode só para cerificação
		boolean c = false;
		if(c = true) {
			System.out.println("Coisas de certificação");
		}
		
		// IF ELSE
				//Estrutura: if(condição booleana) {} else  {} 
				if(false) {
					System.out.println("tegdgcgcd");
				} else  {
					System.out.println("ffdfdfdfd");
				}
		
		
		// ELSE IF
		//Estrutura: if(condição booleana) {} else if () {} 
		if(false) {
			System.out.println("tegdgcgcd");
		} else if (true) {
			System.out.println("ffdfdfdfd");
		}
		
		
		
		
	}

}
